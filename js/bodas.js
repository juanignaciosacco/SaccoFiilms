function pintarImagenes(arrayImagenes, categoria){
    btnVolver.innerHTML = `<a href="bodas.html">Volver</a>`
    titulo.innerText = categoria
    galeriaBodas.innerHTML = ''
    let contandor = 0
    for (e of arrayImagenes){
        contandor++
        let elementos = `<div class="imgContainer d-flex align-items-center justify-content-center col-12 col-sm-6 col-md-3">
                            <button class="btnItem ampliarImagen">
                                <img id="${contandor}" src="${e.img}" class="imgAvance2" alt="${e.descripcion}">
                            </button>
                        </div>`
    galeriaBodas.innerHTML += elementos
    }
    btnImagenes = document.querySelectorAll('.ampliarImagen')
    console.log(btnImagenes)
    for (btn of btnImagenes) {
        btn.onclick = (e) => {
            let imagenAmpliada = e.originalTarget.src
            let id = e.originalTarget.id
            ampliarImg(imagenAmpliada, id, btnImagenes)
        }
    }
}

function ampliarImg(imgUrl, id, arrayImagenes){
    let imgAnterior
    let imgPosterior
    let idN = parseInt(id)

    imgContainer.innerHTML = `<img src="${imgUrl}">`
    visualizadorImg.style.display = 'block'

    flechaDer.onclick = (e) => {
        idN = idN + 1
        console.log(idN)
        arrayImagenes.forEach(imagen => {
            if (imagen.firstElementChild.attributes[0].value == idN){
                imgPosterior = imagen.firstElementChild.attributes[1].value
                idN = parseInt(imagen.firstElementChild.attributes[0].value)
            }
        })
        imgContainer.innerHTML = `<img src="${imgPosterior}">`
    }

    flechaIzq.onclick = (e) => {
        idN = idN - 1
        console.log(idN)
        arrayImagenes.forEach(imagen => {
            if (imagen.firstElementChild.attributes[0].value == idN){
                imgAnterior = imagen.firstElementChild.attributes[1].value
                idN = parseInt(imagen.firstElementChild.attributes[0].value)
            }
        })
        imgContainer.innerHTML = `<img src="${imgAnterior}">`
    }
}